n1 = int(input('Digite um número: '))
n2 = int(input('Digite outro número: '))
som = n1 + n2
sub = n1 - n2
mul = n1 * n2
div = n1 / n2
din = n1 // n2
exp = n1 ** n2
print('A soma é = {}, a subtração é = {}, a multiplicação é = {}'.format(som, sub, mul))
print('A divisão é = {}, a div. inteira é = {}, a exponenciação é = {}'.format(div, din, exp))

